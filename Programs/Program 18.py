x=int(input("Please enter a number: "))

if x > 0:
    if x % 2 ==0:
        print("The number is positive and even")
    else:
        print("The number is positive and odd")
else:
    if x % 2 ==0:
        print("The number is negative and even")
    else:
        print("The number is negative and odd.") 
